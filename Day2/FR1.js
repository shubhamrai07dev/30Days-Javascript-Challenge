// Declare and initialize two variables with numbers
let num1 = 200;
let num2 = 10;

// divide the two numbers
let sum = num1 + num2
let sub = num1 - num2;
let mul = num1 * num2;
let divide = num1 / num2;
let remain = num1 % num2;

// Log the result to the console
console.log(`The addition of ${num1} and ${num2} is : ${sum}`);
console.log(`The subtraction of ${num1} and ${num2} is : ${sub}`);
console.log(`The multiply of ${num1} and ${num2} is : ${mul}`);
console.log(`The division of ${num1} and ${num2} is : ${divide}`);
console.log(`The remainder of ${num1} and ${num2} is : ${remain}`);

