const number = 1212;
const string = "js Developer";
const bool = true;
const obj = { name: "shubham", age: 20 };
const arr = [1, "rai", true];
const newArr = [1, 2, 3, 4, 2, 6];

console.log(typeof (number), number); //number, 1212
console.log(typeof (string), string); //string, js Developer
console.log(typeof (bool), bool); //Boolean, true
console.log(typeof (obj), obj); //object, {name: 'shubham', age: 20}
console.log(typeof (arr), arr); //object, [1, "rai", true]
console.log(typeof (newArr), newArr); //object, [1, 2, 3, 4, 2, 6]
